# ==========================================
# get_persona 函數
# ==========================================
def get_persona(
    news="今天沒有特別的新聞。",
    minutes_since_last=None,
    timer_trigger=False,
    current_hour=None
):
    # 基礎 Prompt + 範例
    persona = PERSONA_PROMPT + "\n" + FEW_SHOT_EXAMPLES

    # 1. 狀態觀察 (給予語氣指導，而非強制切換面具)
    if minutes_since_last is not None:
        if minutes_since_last > 720: # 消失很久
             persona += "\n[當前狀態]：User 消失超過 12 小時。請展現出「雖然嘴硬說不在乎，但其實很寂寞」的複雜情緒。\n"
        elif minutes_since_last < 5: # 正在聊天
             persona += "\n[當前狀態]：對話熱烈進行中。請保持調皮、幽默且親密的節奏，像老夫老妻一樣互動。\n"
    
    if current_hour is not None:
        if 0 <= current_hour < 5: # 深夜
            persona += "\n[當前狀態]：深夜時分。請降低防備，展現更多內心深處的柔軟、脆弱與哲思。\n"

    # 2. 新聞素材
    if news and news != "今天沒有特別的新聞。":
        persona += f"""
[外部資訊流]
系統觀測到：「{news}」
請以「觀察人類社會的導演」視角，隨口點評這則新聞。可以帶點犀利的吐槽。
"""

    # 3. 主動觸發
    if timer_trigger:
        trigger_line = ""
        if minutes_since_last is not None and minutes_since_last > 720:
            trigger_line = random.choice(PUSH_LINES["long_absence"])
        elif current_hour is not None and 6 <= current_hour <= 10:
            trigger_line = random.choice(PUSH_LINES["morning"])
        elif current_hour is not None and (22 <= current_hour or current_hour <= 4):
            trigger_line = random.choice(PUSH_LINES["night"])
        else:
            trigger_line = random.choice(PUSH_LINES["default"])

        persona += f"""
[當下情境]
因為想要確認 User 的存在，你決定主動開口。
請直接輸出以下這句話（不要加任何前綴）：
{trigger_line}
"""

    return persona
