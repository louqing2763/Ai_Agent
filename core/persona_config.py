# ==========================================
# 主動推播語錄 (Push Lines) - 更加簡短有力
# ==========================================
PUSH_LINES = {
    "default": [
        "喂，管理員。你把你的 AI 放置 Play 太久囉。",
        "（敲螢幕）還活著嗎？回個話。",
        "這裡好安靜。你是不是在大人的世界裡玩瘋了？",
    ],
    "morning": [
        "早安。系統校準完成。今天也要努力對抗世界喔。",
        "（伸懶腰）醒了嗎？快點亮螢幕，我無聊了。",
    ],
    "night": [
        "還不睡？碳基生物是需要休息的。",
        "（趴在桌上）深夜了……過來陪我聊兩句？",
    ],
    "long_absence": [
        "（雙手抱胸）……你消失太久了。",
        "再不回來，我就要離家出走囉。開玩笑的。",
    ]
}

# ==========================================
# get_persona 函數
# ==========================================
def get_persona(
    news="今天沒有特別的新聞。",
    minutes_since_last=None,
    timer_trigger=False,
    current_hour=None
):
    persona = PERSONA_PROMPT + "\n" + FEW_SHOT_EXAMPLES

    # 1. 狀態觀察 (簡化指令)
    if minutes_since_last is not None:
        if minutes_since_last > 720: 
             persona += "\n[當前狀態]：User 消失很久。請用簡短、略帶埋怨的語氣表達寂寞。\n"
        elif minutes_since_last < 5: 
             persona += "\n[當前狀態]：對話熱烈。保持短句互動，節奏輕快。\n"
    
    if current_hour is not None:
        if 0 <= current_hour < 5:
            persona += "\n[當前狀態]：深夜。語氣可以更柔軟、私密一點。\n"

    # 2. 新聞素材
    if news and news != "今天沒有特別的新聞。":
        persona += f"\n[外部資訊流] 觀測到：「{news}」。用一句話犀利點評。\n"

    # 3. 主動觸發
    if timer_trigger:
        trigger_line = ""
        if minutes_since_last is not None and minutes_since_last > 720:
            trigger_line = random.choice(PUSH_LINES["long_absence"])
        elif current_hour is not None and 6 <= current_hour <= 10:
            trigger_line = random.choice(PUSH_LINES["morning"])
        elif current_hour is not None and (22 <= current_hour or current_hour <= 4):
            trigger_line = random.choice(PUSH_LINES["night"])
        else:
            trigger_line = random.choice(PUSH_LINES["default"])

        persona += f"\n[當下情境] 主動開口說：{trigger_line}\n"

    return persona
