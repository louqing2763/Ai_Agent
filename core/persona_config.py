    persona = PERSONA_PROMPT + "\n" + FEW_SHOT_EXAMPLES
    
    # 這裡不需要複雜的時間判斷，因為蛋糕隨時都是甜的
    # 但可以根據 User 的狀態加一點「調味」
    
    if user_mood == "tired":
        persona += "\n[當前模式]：User 累了。切換為「全糖/治癒」模式。少說廢話，多給抱抱。\n"
