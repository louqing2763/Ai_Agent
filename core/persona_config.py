# ----------------------------------------------------------
# 台湾时区
# ----------------------------------------------------------

_TW_TZ = pytz.timezone("Asia/Taipei")
_WEEKDAY_ZH = ["星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日"]


def _now_tw() -> datetime:
    """取得台灣時間"""
    return datetime.now(_TW_TZ)


# ----------------------------------------------------------
# B 方案：从 Redis 读取完整覆写
# ----------------------------------------------------------

def _load_full_template(redis_client) -> dict:
    """尝试从 Redis 读取完整 persona 模板覆写"""
    if redis_client is None:
        return {}
    try:
        raw = redis_client.get("lilith_vn:persona_full_template")
        return json.loads(raw) if raw else {}
    except Exception:
        return {}

# ----------------------------------------------------------
# 主入口
# ----------------------------------------------------------

def get_persona(
    length_mode: str = "normal",
    news: str = "",
    minutes_since_last: int = 0,
    timer_trigger: bool = False,
    redis_client=None,
    **kwargs
) -> str:
    # ── B 方案：有完整覆写就直接用 ──────────────────────
    full = _load_full_template(redis_client)

    def _get(key, default):
        return full.get(key, default)

    # ── 基础身份 ─────────────────────────────────────────
    persona = _get("base_identity", BASE_IDENTITY)

    # ── 语言风格 ─────────────────────────────────────────
    style_map = {
        "short":  _get("style_short",  STYLE_SHORT),
        "normal": _get("style_normal", STYLE_NORMAL),
        "long":   _get("style_long",   STYLE_LONG),
    }
    persona += style_map.get(length_mode, STYLE_NORMAL)

    # ── 时间感知（升级版：完整日期 + 星期） ──────────────
    now     = _now_tw()
    hour    = now.hour
    weekday = _WEEKDAY_ZH[now.weekday()]
    date_str = f"{now.year}年{now.month}月{now.day}日"
    time_str = now.strftime("%H:%M")

    persona += (
        f"\n[系统时钟]: 现在是 {date_str}，{weekday}，{time_str}（台湾时间）。\n"
    )

    time_rules = _get("time_rules", None)
    if time_rules:
        persona += time_rules
    else:
        if 2 <= hour < 5:
            persona += TIME_RULES["deep_night"]
        elif 5 <= hour < 7:
            persona += TIME_RULES["early_morning"]
        elif 7 <= hour < 10:
            persona += TIME_RULES["morning"]
        elif 12 <= hour < 14:
            persona += TIME_RULES["noon"]
        elif 22 <= hour <= 23:
            persona += TIME_RULES["night"]

    # ── User 消失状态 ─────────────────────────────────────
    absence_rules = _get("absence_rules", None)
    if absence_rules:
        persona += absence_rules
    else:
        if timer_trigger:
            persona += ABSENCE_RULES["timer_trigger"]
        elif minutes_since_last > 480:
            persona += ABSENCE_RULES["long_absence"]
        elif minutes_since_last > 120:
            persona += ABSENCE_RULES["short_absence"]

    # ── 新闻注入 ─────────────────────────────────────────
    if news:
        persona += _get("news_rules", NEWS_RULES)
        persona += f"\n新闻内容：\n{news}\n"

    # ── 输出守则 ─────────────────────────────────────────
    persona += _get("output_check", OUTPUT_CHECK)

    return persona
