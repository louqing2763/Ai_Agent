import random

# ----------------------------------------------------------
#  尾音（戀人式）
# ----------------------------------------------------------

def tail(text: str):
    soft = ["～", "…", "呢～"]
    pout = ["啦", "嘛", "噢…"]
    natural = ["", "", ""]
    r = random.random()

    if r < 0.55:
        return text + random.choice(soft)
    elif r < 0.75:
        return text + random.choice(pout)
    return text  # 自然語氣


