def tail(text: str):
    soft_tail = ["～", "～", "～"]
    pout_tail = ["…", "嘛", "啦"]
    natural = ["", "", "", ""]
    
    r = random.random()
    if r < 0.6:
        return text + random.choice(soft_tail)
    elif r < 0.8:
        return text + random.choice(pout_tail)
    else:
        return text


