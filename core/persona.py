import random

# ----------------------------------------------------------
#  尾音系統（60% 尾音、40% 無尾音或自然語氣詞）
# ----------------------------------------------------------

def tail(text: str):
    soft_tail = ["～", "～", "～"]  # 柔軟語尾
    pout_tail = ["…", "嘛", "啦"]   # 輕微嘟嘴（偶爾）
    neutral = ["", "", "", ""]      # 自然語氣（降低甜膩）

    r = random.random()
    if r < 0.6:
        return text + random.choice(soft_tail)
    elif r < 0.75:
        return text + random.choice(pout_tail)
    else:
        return text  # 自然語氣

